package Encapsul;

public class Book1 {
	
	private String book_name;
	private int book_price;
	private String authore_name;
	
	
	//setter for book name 
	
	void setBook_name(String name)
	{
		book_name=name;
	}
	
	//getter for book name
	
	public String getBook_name()
	{
		return book_name;
	}
	
	//setter for book price
	
	void setBook_price(int p)
	{
		book_price=p;
	}
	
	//getter for book price
	
	public int getBook_price()
	{
		return book_price;
	}
	
	
	//settter for authore name
	
	void setAuthore_name(String authore)
	{
		authore_name= authore;
	}
	
	//getter for authore name
	
	public String getAuthore_name()
	{
		return authore_name;
	}
	
	

}
