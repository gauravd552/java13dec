package ravipratice;

public class Swapping {

	public static void main(String[] args) {
		int a=20;
		int b=25;
		int c=30;
		int d=35;
		int e=40;
		int temp=b;
		int temp2=d;
		
		
		//a=40,b=35,c=30,d=30,e=25
		
		a=e;
		System.out.println(a);
		
		b=d;
		System.out.println(b);
		
		
		c=c;
		System.out.println(c);
		
		d=c;
		System.out.println(d);
		
		e=temp;
		System.out.println(e);
		
		a=temp2;
		System.out.println(a);
		
		c=temp2;
		System.out.println(c);
				
		
		
	}

}
